<template>
    <div style="height: 500px; width: 500px; border: 1px solid red; position: relative;">
        <vue-draggable-resizable v-bind="$attrs" :w="100" :h="100" :parent="true" v-on="$listeners" @dragging="onDrag" @resizing="onResize">
            <p>
                Hello! I'm a flexible component. You can drag me around and you can resize me.<br>
                X: {{ x }} / Y: {{ y }} - Width: {{ width }} / Height: {{ height }}
            </p>
        </vue-draggable-resizable>
    </div>
</template>
 
<script>
import VueDraggableResizable from 'vue-draggable-resizable'

export default {
    name: 'DraggableResizable',
    components: {
        VueDraggableResizable
    },
    data() {
        return {
            width: 0,
            height: 0,
            x: 0,
            y: 0
        }
    },
    methods: {
        onResize(x, y, width, height) {
            this.x = x
            this.y = y
            this.width = width
            this.height = height
        },
        onDrag(x, y) {
            this.x = x
            this.y = y
        }
    }
}
</script>